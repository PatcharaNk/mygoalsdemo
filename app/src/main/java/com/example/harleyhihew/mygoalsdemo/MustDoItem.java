public class MustDoItem {
    private int id;
    private String mustDoText;
    private int checkMustDo;

    public int getCheckMustDo() {
        return checkMustDo;
    }

    public void setCheckMustDo(int checkMustDo) {
        this.checkMustDo = checkMustDo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }



    public String getMustDoText() {
        return mustDoText;
    }

    public void setMustDoText(String mustDoText) {
        this.mustDoText = mustDoText;
    }
}
